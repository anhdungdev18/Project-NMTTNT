CHECKMATE = 1000
STALEMATE = 0
# DEPTH = 1  # Default depth, can be updated dynamically from ChessMain.py

def findBestMove(game_state, valid_moves, return_queue,_depth):
    global next_move
    next_move = None
    random.shuffle(valid_moves)
    DEPTH = _depth
    findMoveMinimaxAlphaBeta(game_state, valid_moves, DEPTH, -CHECKMATE, CHECKMATE,
                             1 if game_state.white_to_move else -1,_depth)
    return_queue.put(next_move)

def findMoveMinimaxAlphaBeta(game_state, valid_moves, depth, alpha, beta, turn_multiplier,_depth):
    global next_move
    if depth == 0:
        return turn_multiplier * scoreBoard(game_state)

    max_score = -CHECKMATE
    for move in valid_moves:
        game_state.makeMove(move)
        next_moves = game_state.getValidMoves()
        score = -findMoveMinimaxAlphaBeta(game_state, next_moves, depth - 1, -beta, -alpha, -turn_multiplier,_depth)
        game_state.undoMove()

        if score > max_score:
            max_score = score
            if depth == _depth:  # Only update next_move at the root level
                next_move = move

        alpha = max(alpha, score)
        if alpha >= beta:
            break  # Alpha-Beta pruning

    return max_score

def scoreBoard(game_state):
    if game_state.checkmate:
        return -CHECKMATE if game_state.white_to_move else CHECKMATE
    elif game_state.stalemate:
        return STALEMATE

    score = 0
    for row in range(len(game_state.board)):
        for col in range(len(game_state.board[row])):
            piece = game_state.board[row][col]
            if piece != "--":
                piece_position_score = 0
                if piece[1] != "K":
                    piece_position_score = piece_position_scores[piece][row][col]
                if piece[0] == "w":
                    score += piece_score[piece[1]] + piece_position_score
                if piece[0] == "b":
                    score -= piece_score[piece[1]] + piece_position_score

    return score

def findRandomMove(valid_moves):
    return random.choice(valid_moves)
